public class Meida {

    public String fileName;
    public String url;
    public String fileType;
    public String level;

    @Override
    public String toString() {
        return "Meida{" +
                "fileName='" + fileName + '\'' +
                ", url='" + url + '\'' +
                ", fileType='" + fileType + '\'' +
                ", level='" + level + '\'' +
                '}';
    }
}
